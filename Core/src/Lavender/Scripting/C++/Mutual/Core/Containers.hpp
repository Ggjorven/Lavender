#pragma once

#include <glm/glm.hpp>

namespace Lavender::Script
{

	typedef glm::vec2 Vec2;		// Float
	typedef glm::vec3 Vec3;		// Float
	typedef glm::vec4 Vec4;		// Float

	typedef glm::uvec2 UVec2;	// Unsigned int
	typedef glm::uvec3 UVec3;	// Unsigned int
	typedef glm::uvec4 UVec4;	// Unsigned int

	typedef glm::ivec2 IVec2;	// Int
	typedef glm::ivec3 IVec3;	// Int
	typedef glm::ivec4 IVec4;	// Int

	typedef glm::dvec2 DVec2;	// Double
	typedef glm::dvec3 DVec3;	// Double
	typedef glm::dvec4 DVec4;	// Double

}